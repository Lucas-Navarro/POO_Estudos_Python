# Maiusculo, minusculo e titulo

curso = "pYtHoN"

print(curso.upper())
print(curso.lower())
print(curso.title())

# Eliminar espaços em branco na String

print(curso.strip()) # Tira todo o espaço
print(curso.lstrip()) # Joga a string para esquerda
print(curso.rstrip()) # Joga a string para direita


# Junções e centralização

print(curso.center(10, "#")) # Centraliza o texto
print(".".join(curso))
